function detectLanguage() {
  const stored = localStorage.getItem('bm-lang');
  if (stored && translations[stored]) return stored;
  const browserLang = (navigator.language || navigator.userLanguage || 'en').toLowerCase();
  if (browserLang.startsWith('it')) return 'it';
  return 'en';
}

function setLanguage(lang) {
  document.documentElement.lang = lang;
  document.documentElement.dataset.lang = lang;
  localStorage.setItem('bm-lang', lang);
  const t = translations[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) el.textContent = t[key];
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key]) el.innerHTML = t[key];
  });
  document.title = lang === 'it'
    ? 'Butterfly Media — Produzione Video Remota in Italia'
    : 'Butterfly Media — Remote Video Production in Italy';
}

function toggleLang() {
  const current = document.documentElement.dataset.lang || 'en';
  setLanguage(current === 'en' ? 'it' : 'en');
}

// Auto-detect on load
document.addEventListener('DOMContentLoaded', () => {
  setLanguage(detectLanguage());
});